/*La sintaxis general de una sentencia de asignación es la siguiente:

    nom_var = expresion; Asigna a la variable nom_var el resultado de la expresión

La sintaxis general para llamar a una función es la siguiente:

    nombre_funcion(dato1, dato2, ...);  Si la función dada no devuelve un valor

    nom_var=nombre_funcion(dato1,dato2,...); Si la funcion devuelve un valor

La sintaxis general para incluir una librería es la siguiente:

    #include <nom_lib.h>

Por tanto, si se quiere utilizar las funciones de la librería matemática se escribirá la siguente línea al inicio del fihero:*/

